// To install React, in the Command Prompt, type:

// npm install -g create-react-app

// To create a new React App, type in the command prompt:

// create-react-app **name of app**

// There's basically 3 parts to a create-react-app:
// 1) react:            The main library
// 2) react-dom:        Allows react to work with the browser and the document-object-model
// 3) react scripts:    Where webpack and configuration is all located.

// To start the Dev Server, type:

// npm start
